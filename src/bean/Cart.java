package bean;

public class Cart {
	int cId;
	
	String cPcode;
	String cBuyer;
	int cCnt;
	public int getcId() {
		return cId;
	}
	public void setcId(int cId) {
		this.cId = cId;
	}
	public String getcPcode() {
		return cPcode;
	}
	public void setcPcode(String cPcode) {
		this.cPcode = cPcode;
	}
	public String getcBuyer() {
		return cBuyer;
	}
	public void setcBuyer(String cBuyer) {
		this.cBuyer = cBuyer;
	}
	public int getcCnt() {
		return cCnt;
	}
	public void setcCnt(int cCnt) {
		this.cCnt = cCnt;
	}
	
	
}
